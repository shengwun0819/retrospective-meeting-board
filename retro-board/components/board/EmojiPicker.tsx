interface EmojiPickerProps {
  onSelect: (emoji: string) => void
  onClose: () => void
}

export default function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(0)

  return (
    <div
      className="absolute bottom-10 left-0 bg-white rounded-2xl shadow-2xl border border-gray-200 z-40 w-64"
      onMouseDown={(e) => e.stopPropagation()}
      onClick={(e) => e.stopPropagation()}
    >
      {/* Category tabs */}
      <div className="flex border-b border-gray-100 px-2 pt-2 gap-0.5">
        {EMOJI_CATEGORIES.map((cat, i) => (
          <button
            key={i}
            role="tab"
            aria-label={`category-${cat.label}`}
            onClick={() => setActiveCategory(i)}
            className={`text-base px-1.5 py-1 rounded-lg transition-colors ${activeCategory === i ? 'bg-gray-100' : 'hover:bg-gray-50'}`}
          >
            {cat.label}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="p-2 h-48 overflow-y-auto">
        <div className="flex flex-wrap gap-0.5">
          {EMOJI_CATEGORIES[activeCategory].emojis.map((emoji) => (
            <button
              key={emoji}
              onClick={() => { onSelect(emoji); onClose() }}
              className="text-xl w-8 h-8 flex items-center justify-center rounded-lg hover:bg-gray-100 transition-colors"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  )
}
